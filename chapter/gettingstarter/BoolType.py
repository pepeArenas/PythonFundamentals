# True or False
# Integers
print(bool(0))  # False
print(bool(1))  # True
print(bool(-11))  # True
# Decimals
print(bool(0.0))  # False
print(bool(1.0))  # True
print(bool(-1.0))  # True
# Strings
print(bool(""))  # False
print(bool("False"))  # True because is not empty no matter if is the string False
# List
print(bool([]))  # False because is an empty list
print(bool([1]))  # True because is not an empty list
